#include "cub3d.h"

int		ft_update(void)
{
	int i;

	i = 0;
	while (i < 7)
	{
		if (g_keys[i])
			deal_key(g_keys[i]);
		i++;
	}
	ft_castAllRays();
	render3d();
	return (0);
}
